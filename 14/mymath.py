
#Write a Python Code to define a USER-DEFINED MODULE to FIND MAXIMUM, MINIMUM, SUM AND
#AVERAGE OF GIVEN “n” NUMBERS.

def maxfind(number):
    return max(number)

def minfind(number):
    return min(number)

def calsum(number):
    return sum(number)

def calavg(number):
    return sum(number)/len(number)
